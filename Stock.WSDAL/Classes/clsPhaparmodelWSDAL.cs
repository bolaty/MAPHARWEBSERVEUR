					clsPhaparmodel clsPhaparmodel = new clsPhaparmodel();
					clsPhaparmodel.MO_CODEMODEL = Dataset.Tables["TABLE"].Rows[Idx]["MO_CODEMODEL"].ToString();
					clsPhaparmodel.MO_LIBELLE = Dataset.Tables["TABLE"].Rows[Idx]["MO_LIBELLE"].ToString();
					clsPhaparmodels.Add(clsPhaparmodel);
				}
				Dataset.Dispose();
			}
		return clsPhaparmodels;
